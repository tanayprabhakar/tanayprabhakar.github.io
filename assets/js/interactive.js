document.addEventListener("DOMContentLoaded", () => {
  // Custom Cursor Logic
  const cursor = document.getElementById("custom-cursor");
  const cursorTrail = document.getElementById("custom-cursor-trail");
  
  let mouseX = 0, mouseY = 0;
  let trailX = 0, trailY = 0;
  
  document.addEventListener("mousemove", (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
    
    // Immediate cursor update
    if (cursor) {
      cursor.style.transform = `translate3d(${mouseX}px, ${mouseY}px, 0)`;
    }
    
    // 3D Tilt Logic for Cards
    const cards = document.querySelectorAll(".page_content");
    const windowWidth = window.innerWidth;
    const windowHeight = window.innerHeight;
    
    // Calculate rotation based on mouse position relative to center of screen
    const rotateY = ((mouseX / windowWidth) - 0.5) * 15; // max 7.5deg
    const rotateX = ((mouseY / windowHeight) - 0.5) * -15;
    
    cards.forEach(card => {
      // Only apply if it's visible or you just want to apply globally
      card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg)`;
    });
  });
  
  // Smooth trail animation
  const animateTrail = () => {
    trailX += (mouseX - trailX) * 0.15;
    trailY += (mouseY - trailY) * 0.15;
    
    if (cursorTrail) {
      cursorTrail.style.transform = `translate3d(${trailX}px, ${trailY}px, 0)`;
    }
    
    requestAnimationFrame(animateTrail);
  };
  animateTrail();
  
  // Hover effect for links
  const links = document.querySelectorAll("a, .theme_btn, .theme_colors");
  links.forEach(link => {
    link.addEventListener("mouseenter", () => {
      if (cursor) cursor.classList.add("hover");
      if (cursorTrail) cursorTrail.classList.add("hover");
    });
    link.addEventListener("mouseleave", () => {
      if (cursor) cursor.classList.remove("hover");
      if (cursorTrail) cursorTrail.classList.remove("hover");
    });
  });

  // Fetch LeetCode Stats
  const fetchLeetCodeStats = async () => {
    try {
      // Fetch stats from local JSON generated by GitHub Actions
      const res = await fetch('/assets/data/leetcode.json');
      if (!res.ok) throw new Error("Failed to load local stats");
      
      const data = await res.json();
      
      const loadingEl = document.getElementById('leetcode-loading');
      const statsListEl = document.getElementById('leetcode-stats-list');
      
      if (loadingEl && statsListEl) {
        loadingEl.style.display = 'none';
        statsListEl.style.display = 'block';
        
        const totalEl = document.getElementById('lc-solved-total');
        if (totalEl) totalEl.innerHTML = `<b>${data.solvedTotal}</b> total solved`;
        
        const easyBar = document.getElementById('lc-bar-easy');
        const mediumBar = document.getElementById('lc-bar-medium');
        const hardBar = document.getElementById('lc-bar-hard');
        
        if (easyBar) easyBar.style.width = `${(data.easy / data.solvedTotal) * 100}%`;
        if (mediumBar) mediumBar.style.width = `${(data.medium / data.solvedTotal) * 100}%`;
        if (hardBar) hardBar.style.width = `${(data.hard / data.solvedTotal) * 100}%`;

        const easyEl = document.getElementById('lc-easy');
        if (easyEl) easyEl.innerText = `Easy: ${data.easy}`;
        
        const mediumEl = document.getElementById('lc-medium');
        if (mediumEl) mediumEl.innerText = `Med: ${data.medium}`;
        
        const hardEl = document.getElementById('lc-hard');
        if (hardEl) hardEl.innerText = `Hard: ${data.hard}`;
        
        const rankEl = document.getElementById('lc-rank');
        if (rankEl && data.rank) {
           rankEl.innerText = `${data.rank.toLocaleString()}`;
        }
        
        const topicsEl = document.getElementById('lc-topics');
        if (topicsEl) topicsEl.innerText = data.topTopics;
      }
    } catch (error) {
      console.warn('Error fetching LeetCode stats, using static fallback:', error);
      
      // Static fallback data for local file:// testing or if fetch fails
      const fallbackData = {
        solvedTotal: 331,
        easy: 122,
        medium: 161,
        hard: 48,
        rank: 418216,
        topTopics: "Array, Hash Table, Math, String, Dynamic Programming, Sorting, Two Pointers, Matrix"
      };

      const loadingEl = document.getElementById('leetcode-loading');
      const statsListEl = document.getElementById('leetcode-stats-list');
      
      if (loadingEl && statsListEl) {
        loadingEl.style.display = 'none';
        statsListEl.style.display = 'block';
        
        const totalEl = document.getElementById('lc-solved-total');
        if (totalEl) totalEl.innerHTML = `<b>${fallbackData.solvedTotal}</b> total solved`;
        
        const easyBar = document.getElementById('lc-bar-easy');
        const mediumBar = document.getElementById('lc-bar-medium');
        const hardBar = document.getElementById('lc-bar-hard');
        
        if (easyBar) easyBar.style.width = `${(fallbackData.easy / fallbackData.solvedTotal) * 100}%`;
        if (mediumBar) mediumBar.style.width = `${(fallbackData.medium / fallbackData.solvedTotal) * 100}%`;
        if (hardBar) hardBar.style.width = `${(fallbackData.hard / fallbackData.solvedTotal) * 100}%`;

        const easyEl = document.getElementById('lc-easy');
        if (easyEl) easyEl.innerText = `Easy: ${fallbackData.easy}`;
        
        const mediumEl = document.getElementById('lc-medium');
        if (mediumEl) mediumEl.innerText = `Med: ${fallbackData.medium}`;
        
        const hardEl = document.getElementById('lc-hard');
        if (hardEl) hardEl.innerText = `Hard: ${fallbackData.hard}`;
        
        const rankEl = document.getElementById('lc-rank');
        if (rankEl) {
           rankEl.innerText = `${fallbackData.rank.toLocaleString()}`;
        }
        
        const topicsEl = document.getElementById('lc-topics');
        if (topicsEl) topicsEl.innerText = fallbackData.topTopics;
      }
    }
  };

  fetchLeetCodeStats();
});
